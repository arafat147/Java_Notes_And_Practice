package basicJava;

public class L_RelationalOperator {

    public static void main(String[] args) {
        /*
        == is equals to
        != is not equals to
        > is grater than
        < is less than
        >= grater than or equals to
        <= less than or equals to
        */

        int a = 50;
        if (a == 50){
            System.out.println("a is equals to 50");
        }
        if (a>40){
            System.out.println("a is grater than 40");
        }
        if (a<60){
            System.out.println("a is less than 60");
        }
        if (a>= 50){
            System.out.println("a is grater than or equals to 50");
        }
        if (a<=50){
            System.out.println("a is less than or equals to 50");
        }
        if (a!=40){
            System.out.println("a is not equals to 40");
        }











    }
}
